const suggestionEl = document.querySelector("#dailySuggestion");
const contentEl = document.querySelector("#content");
const linkEl = document.querySelector("#officialLinks");
const tabs = [...document.querySelectorAll(".tab")];

const escapeHtml = value => String(value)
  .replaceAll("&", "&amp;")
  .replaceAll("<", "&lt;")
  .replaceAll(">", "&gt;")
  .replaceAll('"', "&quot;");

const dayIndex = () => {
  const start = new Date("2026-01-01T00:00:00");
  const today = new Date();
  return Math.floor((today - start) / 86400000);
};

const pick = (items, offset = 0) => items[(dayIndex() + offset) % items.length];

function renderDaily() {
  const items = [
    ["Do", pick(source.daily.do)],
    ["Explore", pick(source.daily.explore, 2)],
    ["Know", pick(source.daily.know, 4)]
  ];

  suggestionEl.innerHTML = items.map(([label, text]) => `
    <article class="suggestion">
      <strong>${escapeHtml(label)}</strong>
      <p>${escapeHtml(text)}</p>
    </article>
  `).join("");
}

function renderView(name) {
  const view = source.views[name];
  contentEl.innerHTML = `
    <div class="section-head">
      <div>
        <p class="eyebrow">${escapeHtml(view.kicker)}</p>
        <h2>${escapeHtml(view.title)}</h2>
      </div>
      <p class="updated">Updated ${escapeHtml(source.meta.updatedAt)} · public-safe</p>
    </div>
    <div class="card-grid">
      ${view.cards.map(card => `
        <article class="item-card" data-type="${escapeHtml(card.type)}">
          <div>
            <strong>${escapeHtml(card.label)}</strong>
            <h3>${escapeHtml(card.title)}</h3>
          </div>
          <p class="meta">${escapeHtml(card.text)}</p>
          <div class="chip-row">
            ${card.tags.map(tag => `<span class="chip">${escapeHtml(tag)}</span>`).join("")}
          </div>
        </article>
      `).join("")}
    </div>
  `;
}

function renderLinks() {
  linkEl.innerHTML = source.links.map(([label, href]) => `
    <a href="${escapeHtml(href)}" rel="noreferrer" target="_blank">
      <span>${escapeHtml(label)}</span>
      <span>Open</span>
    </a>
  `).join("");
}

tabs.forEach(tab => {
  tab.addEventListener("click", () => {
    tabs.forEach(button => button.classList.toggle("is-active", button === tab));
    renderView(tab.dataset.view);
  });
});

renderDaily();
renderView("fresh");
renderLinks();

